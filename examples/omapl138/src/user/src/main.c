void flash_led(void)
{
    u32 out = 0;
    void *my_para;
    while(1)
    {
//        my_para = djy_get_event_para(NULL);
        if((out &1) == 1)
        {
            gpio_setto_low(7,0x2);
        }else
        {
            gpio_setto_high(7,0x2);

        }
        out++;
        //执行本句后，事件将被阻塞，由于被同步的事件类型就是本事件的事件类型，
        //所以同步条件就是：再次发生本类型事件。
        //参数 ‘1’表示只要发生一次事件就可以了
        //参数500表示如果一直不发生，500mS后结束等待
        //关于事件类型弹出同步，参见《都江堰操作系统与嵌入式系统设计》的第5.3节
        djy_wait_evtt_pop(pg_event_running->evtt_id,1,500*mS);
    }
}

