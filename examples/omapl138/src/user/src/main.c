void djy_main(void)
{
    struct keyboard_msg *key;
    u32 time,x,y;
    char buf[20];
    define_align_buf(para_buf,32);
    struct gksc_para_draw_bitmap bm_para;
    struct gksc_para_create_gkwin cw_para;
    struct display_rsc *dsp2416;
    struct gkwin_rsc *result;
    struct st_rect_bitmap logo;
    static struct gkwin_rsc *desktop,gkwin1,gkwin2;
    uint16_t evtt_flash_led;
    static struct display_rsc *omapl138_gray1;
    u32 parameter,i;

    evtt_flash_led = djy_evtt_regist(enum_correlative,cn_prio_RRS,0,0,1,
                                        flash_led,1000,NULL);
    parameter = 1;
    djy_event_pop(evtt_flash_led,NULL,0,&parameter,
                    sizeof(parameter),false,0);
    desktop = gk_api_get_desktop("lcd_omapl138");
    logo.bm_bits = (u8*)gImage_djy320240;
    logo.height = 240;
    logo.width = 320;
    logo.linebytes = 40;
    logo.pf_type = cn_sys_pf_gray1;
    gk_api_draw_bitmap(desktop,&logo,0,0,cn_gk_sync);

    stdin_set_focus("key board",djy_my_evtt_id());
    while(1)
    {
        djy_wait_evtt_pop(djy_my_evtt_id(),1,cn_timeout_forever);
        key = (struct key_board_msg *)djy_get_event_para(&time);
        djy_printf("按键时间：%d",time);
        djy_printf(" 键值 %d  %d\r\n",key->key_value[1],key->key_value[0]);
    }
}

